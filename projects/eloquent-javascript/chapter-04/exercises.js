////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1) {

let output = [];
if (start === end) {
  return output;
}
if (step > 0) {
  for(let i = start; i <= end; i += step) {
    output.push(i);
  }
} else if (step < 0) {
  for(let i = start; i >= end; i += step) {
    output.push(i);
  }
}

return output;
}


// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(a, b) {
  //ternery!
return a < b ? a : b;
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
if (x === 0) {
  return true;
} else if (x === 1) {
  return false;
} else if (x < 0) {
  return isEven(-x);
} else {
  return isEven(x - 2);
}
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
let output = 0;
for (let i = 0; i < string.length; i++) {
  if (string[i] == char) {
    output += 1;
  }
}
return output;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
return countChars(string, 'B');
}

